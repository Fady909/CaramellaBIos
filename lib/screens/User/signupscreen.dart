أي عضو في تطبيق كاراميلا يحصل على "اسم مستخدم" و"كلمة سر" يتم تحديدها بواسطة العضو نفسه. إن "اسم المستخدم" شخصي ويقتصر على العضو وحده، ونفس "اسم المستخدم" لا يتم منحه إلى عضوين مختلفين. كما أن "كلمة السر" تكون معلومة للمستخدم وحده. ويمكن للمستخدم تغيير كلمة السر في أي وقت. كما أنه من مسؤولية المستخدم وحده اختيار وحماية كلمة السر. إن تطبيق كاراميلا غير مسؤولة عن أي مشاكل قد تنشأ عن استخدام كلمة السر. وتقوم تطبيق كاراميلا بإبلاغ المستخدم عن العروض التي تتم في منطقته والتي يمكن للمستخدم الاستفادة منها عن طريق الرسائل الالكترونية. إن تطبيق كاراميلا لا تشارك معلومات المستخدم مع الغير من الشركات، وقد يتم مشاركة رقم الهاتف مع المحل الذي يتعامل مع الطلبات الخارجية ، لكي يتم تقديم خدمة توصيل سريعة وصحيحة. ولكي يقوم المستخدم بالاستفادة من خدمات تطبيق كاراميلا التي تتطلب عضوية في تطبيق كاراميلا، يتعين على المستخدم إدخال اسم المستخدم وكلمة السر. ويطلق على هذه العملية "تسجيل الدخول" الى تطبيق كاراميلا

عند استخدام التطبيق، فأنت تضمن أن أية معلومات شخصية مقدمة منك صحيحة ودقيقة وسارية المفعول وكاملة من كافة النواحي وانك سوف تقوم بإبلاغنا فورا بأي تغييرات على معلوماتك الشخصية عن طريق تحديث معلومات العضوية الخاصة بك على التطبيق أو عن طريق الاتصال بنا. وتوافق على عدم تقمص شخصية أي شخص او جهة اخرى او استخدام اسم مزور او اسم لا تكون مصرح باستخدامه
لا يسمح لك بتسجيل عضويات متعددة للاستخدام بواسطة نفس الشخص. وقد ينتج عن انتهاك هذا البند انهاء عضويتك
يجب الاحتفاظ بكلمات السر الخاصة بالاعضاء في سرية تامة في كافة الاوقات ولا يجب مشاركتها أو الكشف عنها إلى أي شخص آخر. انت مسؤول عن كافة الانشطة والطلبات التي تحدث او التي يتم تقديمها عن طريق عضويتك. واذا اصبحت على علم او اشتبهت في ان هناك شخص يعلم كلمة السر الخاصة بك، عندئذ يتعين عليك الاتصال بنا فورا.
