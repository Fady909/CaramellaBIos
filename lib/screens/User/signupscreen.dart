ارسال مواد تشكل مخالفة للقانون أو الأديان أو العقائد والمذاهب أو الأخلاق، وينتج عنها مسؤولية مدنية او خلاف ذلك تنتهك أي قوانين ولوائح أو أنظمة أو ممارسات
التدخل باستخدام او استمتاع أي شخص بالتطبيق
