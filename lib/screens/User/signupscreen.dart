نتوقع أن يحمل كل جهاز حساب عميل واحد فقط. في مثل هذه الحالات، قد يُسمح للعميل باستخدام الخدمة ولكنه لن يتمكن من الاستمتاع بالعروض الترويجية.

خلال استخدام خدمات تطبيق كاراميلا، يقبل العضو ويقر بما يلي :
ان المعلومات الواردة في استمارة التسجيل صحيحة، بشرط ان تكون هذه المعلومات مطلوبة، وأن العضو هو المسؤول وحده عن أية خسائر قد تنشأ عن أية معلومات خاطئة او معلومات غير كافية (على سبيل المثال نسيان كلمة السر) وفي تلك الحالات يتم انهاء عضويته.
ان العضو هو وحده المسؤول عن كافة الافكار والاراء والبيانات الشخصية التي يذكرها اثناء استخدامه خدمات تطبيق كاراميلا، وكذلك مسؤولا عن كافة الملفات التي يتم تحميلها الى تطبيق كاراميلا والمعلومات الشخصية المرسلة، وإن تطبيق كاراميلا لن تكون مسؤولة بأي شكل من الأشكال عن تلك الملفات، أو عن الولوج الى أي خدمات مقدمة عبر تطبيق كاراميلا يكون غير مصرح بها أو بأي طريقة أخرى غير تلك المحددة بواسطة تطبيق كاراميلا ، وعدم تغيير البرمجيات باي طريقة، وتعويض تطبيق كاراميلا عن كافة الخسائر المادية والادبية في حالة عدم التزام العضو بالمواد المذكورة اعلاه. ولن تكون تطبيق كاراميلا مسؤولة عن أي اضرار قد تنشأ عن قراءة البيانات العائدة الى أي أعضاء بواسطة أي افراد غير مصرح لهم. ويجب على العضو عدم ارسال أي رسائل تكون تهديدية، أو غير أخلاقية، أو عنصرية أومناقضة لقوانين الاردن والاتفاقيات الدولية. وأي مراسلات أو عناوين أو اسماء مستعارة يجب ألا تكون مخالفة للوائح والآداب العامة والسلوك الحسن والقوانين. ويلتزم بعدم مضايقة و/أو تهديد المستخدمين الآخرين، أو أي من موظفي خدمة العملاء التابعين لـتطبيق كاراميلا ، وعدم التصرف بطريقة تؤثر على استخدام الخدمات من قبل المستخدمين الاخرين، وعدم ارسال، طباعة، أو توزيع أو تعميم أية مواد لا أخلاقية، غير مناسبة وغير قانونية أومعلومات قد تسبب ضررا بأسماء أي أشخاص أو مؤسسات، أو الاعلان، أو البيع أو العرض لبيع أي منتجات أو خدمات، أو المشاركة في أي أفعال ضارة مثل المسح، أو المنافسة أو الرسائل المتسلسلة، وعدم ارسال أي معلومات أو برامج قد تؤدي الى الضرر بالمعلومات أو البرمجيات على أجهزة المستخدمين الآخرين. وكافة السجلات أو المواد التي يتم الحصول عليها بواسطة استخدام خدمات تطبيق كاراميلا تكون ضمن موافقة المستخدم، وسوف يكون المستخدم مسؤولا تماماً عن أي اضرار أو فقد المعلومات أو أي خسائر اخرى ناتجة عن ذلك لجهاز كمبيوتر المستخدم، ولا يحق للمستخدم المطالبة بالتعويض عن أي خسائر تنشأ عن استخدام الخدمة، كما يلتزم بعدم استخدام خدمات تطبيق كاراميلا لاي اغرض تجارية أواعلانية بدون الحصول على موافقة تطبيق كاراميلا.
ا. قد تقوم تطبيق كاراميلا بمراقبة كافة الانظمة في أي وقت او باستمرار
ب. قد تقوم تطبيق كاراميلا باستخدام نظامها لاغراض تجارية.
عدم ارسال أي معلومات بالبريد الإلكتروني تكون محظورة قانونيا أوتوزيع أي رسائل غير معتمد ارسالها مثل رسائل البريد المتسلسلة والبرامج الضارة الخ، وعدم تسجيل واساءة استخدام أي معلومات شخصية عائدة الى الاخرين. ان العضو نفسه مسؤول عن كافة انواع التصرفات التي يتم القيام بها تحت اسم "المستخدم". ويتعين على الاعضاء دفع رسوم الخدمة التي تم طلبها مسبقا عند تسليم المنتجات، وخلاف ذلك لن يتم تسليم المنتجات الى العملاء، وعدم الولوج الى التطبيق او الخدمات باستخدام حساب/تسجيل طرف ثالث بدون موافقة صريحة من حامل الحساب، وعدم استخدام التطبيق لاغراض غير قانونية، وعدم ارتكاب أي تصرفات مخالفة على التطبيق او فيما يخص المحتوى على التطبيق، وعدم استخدام التطبيق للقيام بأي انشطة تجارية غير الاستخدام المصرح له لخدمات تطبيق كاراميلا ، وعدم نسخ أي محتوى، بما في ذلك وبدون حصر محتوى قوائم المحلات ومراجعات الغير، لاعادة النشر بشكل مطبوع او عبر الانترنت، وعدم انشاء مراجعات المحل او تسجيل أي ادخالات لصالح او مع أي غرض تجاري او غرض اخر او قصد لا يتفق بنية حسنة مع غرض إنشاء التطبيق، وعدم محاولة الحصول على حرية دخول غير مصرح بها الى انظمة كمبيوتر الاخرين من او عن طريق التطبيق وعدم التدخل في استخدام شخص آخر أو جهات أخرى للتطبيق ، وعدم تحميل او نقل الفيروسات او الملفات الضارة والتخريبية او التدميرية الاخرى، و/او
عدم تعطيل، التدخل في او حتى التسبب في ضرر أو مخالفة أمن التطبيق، او أي خدمات، البرمجيات او الأجهزة المرتبطة بالنظام، الحسابات، كلمات السر، الخوادم او الشبكات المرتبطة بالتطبيق أو التي يتم الدخول إليها عن طريق المواقع أو المواقع الزميلة أو الموصلة (بما في ذلك تلك العائدة الى شركائنا من المحلات).
يتعين على كافة المستخدمين من القصر في المكان الذي يقيمون فيه (عموما تحت 18 سنة) الحصول على تصريح من ولي أمرهم، ويتم الاشراف عليهم مباشرة بواسطة ولي امرهم او الاب /الام عند استخدام التطبيق. اذا كنت قاصر، يتعين على ولي امرك/ والدك قراءة هذه الاتفاقية والموافقة عليها قبل قيامك باستخدام التطبيق على الشبكة.
بطاقات الإئتمان أو الدفع المسبق التي يستخدمها العميل في السداد من خلال بوابة الدفع الإلكتروني على تطبيق كاراميلا يتوجب أن تعود ملكيتها للعميل نفسه. في حال كانت ملكية البطاقة تعود لشخص آخر فيتوجب على العميل إستخدامها بصورة قانونية وذلك بموافقة صاحب البطاقة.


خلاف المعلومات الشخصية، التي تم تغطيتها في سياسة الخصوصية لـ تطبيق كاراميلا ، فإن أي مواد تقوم بارسالها او نشرها على هذا التطبيق على الشبكة سوف تعتبر غير سرية وغير شخصية. ولن يكون على تطبيق كاراميلا أي التزامات فيما يخص هذه المواد. وسوف يكون لـتطبيق كاراميلا واي شخص نقوم بتعيينه الحرية لنسخ وتصحيح وتوزيع وتضمين أو أي استخدام خلاف ذلك لهذه المواد وكافة البيانات والصور والاصوات والنصوص والاشياء الاخرى المشمولة فيه لأي ولكافة الاغراض التجارية او غير التجارية.
يحظر عليك نشر وتحميل أو ارسال إلى أو من التطبيق على الشبكة أي من المواد التالية:
تنتهك أي قانون محلي، وطني او دولي معمول به.
غير مشروعة او احتيالية.
تصل الى حد الاعلان غير المصرح به او تحتوي على فيروسات او أي برامج اخرى ضارة.
لا يجوز لك اساءة استخدام التطبيق (بما في ذلك بواسطة القرصنة)
أي تعليقات أو ملاحظات يضعها المستخدم على التطبيق يجب ألا:
تحتوي على أي مواد تشهيرية، خليعة او مخالفة.
تعزز العنف او التمييز.
تنتهك حقوق الملكية الفكرية لشخص اخر.
تنتهك أي واجب قانوني تجاه طرف ثالث (مثل واجب السرية).
تعزز النشاط غير القانوني او تخترق خصوصية الاخر.
تقدم انطباعا انها صادرة عنا اويتم استخدامها لتقمص شخصية شخص اخر او تقديم معلومات خاطئة عن ارتباطك بشخص اخر.
إن التصرفات المحظورة الواردة في الفقرات اعلاه غير حصرية. وسوف تقوم بتعويض تطبيق كاراميلا عن كافة النفقات والاضرار التي تتحملها كنتيجة لانتهاكك لأي من تلك القيود.
سوف تقوم تطبيق كاراميلا بالتعاون بالكامل مع أي جهات تنفذ القانون او قرارات المحكمة التي تطلب منا او توجهنا للتصريح عن هوية أو تطبيق أي شخص يقوم بنشر أي مواد بمخالفة الفقرات المذكورة أعلاه 

ا يجوز لك الدخول الى او استخدام التطبيق لاي غرض اخر بخلاف ذلك الذي تقوم تطبيق كاراميلا بتوفيره. ان التطبيق هو للاستخدام الشخصي للمستخدمين فقط ولا يجوز استخدامه بالارتباط مع أي مصالح تجارية باستثناء تلك التي تم اقرارها او اعتمادها تحديدا من تطبيق كاراميلا. في حال إرتكاب المستخدم أي أنشطة محظورة، فإن الـ لديها الحق في تعليق، أو منع أو إنهاء عضوية المستخدم ومنع دخوله إلى الخدمة. تتضمن الانشطة المحظورة ولا تقتصر على ما يلي:
النشاط الجنائي او التقصيري، بما في ذلك الافلام الخلاعية للاطفال، الاحتيال، التجارة في المواد الخليعة، الاتجار في المخدرات، القمار، التحرش، المطاردة، البريد الالكتروني غير المرغوب فيه، انتهاك حقوق النشر، انتهاك براءات الاختراع او سرقة الاسرار المهنية.
‌الاعلان او استدراج أي شخص لشراء او بيع أي منتجات او خدمات، ما لم يكن ذلك مصرح من تطبيق كاراميلا.
‌الاستعادة النظامية للبيانات او المحتويات الاخرى من التطبيق على الشبكة لانشاء او تأليف، بشكل مباشر أو غير مباشر، مجموعة، مؤلف ، قاعدة بيانات، او دليل بدون أي تصريح خطي من تطبيق كاراميلا.
‌القيام بأي استخدام غير مصرح به لخدمات تطبيق كاراميلا ، بما في ذلك جمع اسماء المستخدمين و/أو عناوين البريد الالكتروني للمستخدمين بواسطة وسائل الكترونية او اخرى لغرض ارسال بريد الكتروني غير مرغوب فيه.
‌‌المشاركة في أي صياغة غير مصرح بها او ربطها بالتطبيق.
‌ارسال رسائل متسلسلة او بريد الكتروني غير مرغوب فيه الى المستخدمين الآخرين.
‌استخدام أي معلومات يتم الحصول عليها من التطبيق على الشبكة لكي يتم الاتصال، الاعلان عن، او استدراج او البيع الى أي مستخدم بدون موافقته الصريحة المسبقة.
‌المشاركة في أي استخدام اتوماتيكي للنظام مثل ارسال النصوص لاضافة الاصدقاء او ارسال الملاحظات او الرسائل او استخدام أي ادوات استخراج بيانات او ادوات جمع واستخراج بيانات اخرى.
‌التدخل في، تعطيل او انشاء عبء غير مبرر على التطبيق او الشبكات او الخدمات المرتبطة بالتطبيق.
‌‌محاولة تقمص شخص او مستخدم اخر.
‌‌استخدام اسم المستخدم العائد الى شخص اخر.
‌‌بيع او بخلاف ذلك تحويل البيانات الخاصة بك.
‌استخدام أي معلومات تم الحصول عليها من التطبيق لكي يتم مضايقة، الاساءة الى او الضرر بشخص اخر.
‌استخدام خدمة تطبيق كاراميلا كجزء من أي جهد لمنافسة تطبيق كاراميلا او تقديم الخدمات كمكتب خدمة.
‌فك شفرة، فك تأليف، تفكيك او عكس هندسة أي من البرمجيات التي يتكون منها التطبيق أو تشكل جزءا من التطبيق.
‌محاولة تجاوز أي اجراءات للتطبيق مصممة لمنع او تقييد الدخول الى التطبيق او أي جزء من التطبيق.
مضايقة، ازعاج، ترهيب او تهديد لـ تطبيق كاراميلا من خلال أي وسيلة إتصال أو لأي موظفي او وكلاء تابعين لـ تطبيق كاراميلا أو من يشاركون في تقديم أي جزء من خدمات تطبيق كاراميلا.
‌عرض اعلان او قبول مدفوعات او أي شيء ذي قيمة من الغير مقابل قيامك بأداء أي نشاط تجاري على او عن طريق التطبيق نيابة عن ذلك الشخص، مثل نشر البلوجات أو النشرات ذات الغرض التجاري.
‌حذف حقوق النشر او اشعار حقوق الملكية الاخرى من أي مساهمة او محتوى على تطبيق كاراميلا.
‌‌استخدام التطبيق باي اسلوب لا يتفق مع أي وكافة القوانين واللوائح المعمول بها.
أي إشتباه بالقيام بعمليات غير مشروعة على تطبيق وتطبيقات تطبيق كاراميلا

يجوز ان تقوم تطبيق كاراميلا بشكل مؤقت بتوقيف او التوقف تماما عن تشغيل النظام في أي وقت.
لن يكون على تطبيق كاراميلا أية مسؤوليات تجاه اعضاء تطبيق كاراميلا او أي طرف ثالث بسبب التوقيف المؤقت او التوقف الكامل لتشغيل النظام.
سوف يتم عرض اسم المستخدم وكلمة السر واعتماده بعد تعبئة بيانات النموذج الذي سيتم تقديمه الى العملاء بواسطة تطبيق كاراميلا . ويجوز ان تقوم تطبيق كاراميلا بمنع عملائها الذين قاموا بتعبئة بيانات النموذج أو تواصلوا مع التطبيق، بالرغم من ان لديهم كلمات سر من الحصول على كلمة سر جديدة او من استخدام كلمات السر الخاصة بهم لمدة زمنية غير محددة؛ اذا اعتبرت تطبيق كاراميلا ذلك ضروري.
لا تضمن تطبيق كاراميلا أن خدماتها سوف يتم تقديها في الوقت المحدد بطريقة آمنة وتامة، وان النتائج التي سيتم الحصول عليها من الخدمات سوف تكون صحيحة وموثوق فيها، وان نوعية الخدمات سوف ترقى إلى مستوى التوقعات، حيث ان دور تطبيق كاراميلا ينحصر في إخطار المحل بطلبات العميل.
يحق لـتطبيق كاراميلا ان تقوم بدعم وحذف كافة او بعض الملفات والرسائل التي يتم تقديمها بواسطة الاعضاء أثناء استخدام الخدمات لبعض الوقت وذلك خلال الفترات التي تعتبر مناسبة بواسطة تطبيق كاراميلا . ولن تكون تطبيق كاراميلا مسؤولة عن اجراءات الدعم والحذف.
إن تطبيق كاراميلا لديها حقوق الملكية والنشر الناشئة عن ملكية المعلومات والمستندات والبرمجيات والتصميمات والرسوم البيانية الخ، التي تقوم بانتاجها بنفسها و/او يتم شراءها من الخارج.
وتطبيق كاراميلا ليست مسؤولة عن أي بضاعة يتم طلبها ولم يتم توصيلها من قبل المحل بسبب عدم توفرها
يحق لـتطبيق كاراميلا نشر أي معلومات ومستندات وبرمجيات وتصميمات ورسوم بيانية الخ (مثل الرسائل والاشعار والتقارير/الاخبار والملفات المرسلة الى النشرات) التي يتم انتاجها بواسطة الاعضاء وتحميلها الى النظام بواسطة الاعضاء و/أو يتم ارسالها الى مكان اخر ضمن التطبيق يعتبر ملائم بواسطة تطبيق كاراميلا ومن المحتمل ان يتم نسخ و/او نشر هذه المعلومات بواسطة المستخدمين الاخرين. وفي تلك الاحوال، لن يطلب المستخدمين أي اتعاب من تطبيق كاراميلا.
ان الالتزام بتغيير المعلومات المتعلقة بالسعر ومواصفات المنتج التي يتم تقديمها للبيع عبر تطبيق كاراميلا يعود الى الشركات التي يكون لديها محلات مشتركة مع تطبيق كاراميلا. وفي حالة أية معلومات خاطئة تتعلق بالسعر ومواصفات المنتج، يجوز ان تقوم تطبيق كاراميلا بتصحيح الخطأ عن طريق الغاء الطلب.
يجوز ان تقوم تطبيق كاراميلا بارسال العضو الى تطبيق اخر على الشبكة. وفي هذه الحالة، يوافق العضو على ان تطبيق كاراميلا لن تكون مسؤولة عن سياق محتوى التطبيق الذي يجوز للعضو الولوج اليه.
يجوز ان تقوم تطبيق كاراميلا باي تغييرات على تنفيذ هذه الاتفاقية وتعديل المواد القائمة او اضافة مواد جديدة لغرض الالتزام بأي التزامات فنية وتشريعات يتم اصدراها في المستقبل.
يجوز ان تقوم تطبيق كاراميلا بتغيير الخدمات التي لا تتطلب عضوية الى شكل يتطلب عضوية. ويجوز ان تقوم تطبيق كاراميلا بتقديم خدمات اضافية وتغيير بعض الخدمات جزئيا او بالكامل او تحويلها الى خدمة مدفوعة.
يجوز ان يكون هذا التطبيق غير متاح في بعض الاوقات للسماح بعمليات الصيانة والتحديث. وبالرغم من اننا سوف نسعى لابلاغ العملاء مسبقا بعدم توفر أي خدمة مذكورة، الا ان ذلك لا يمكن ضمانه ونحتفظ بحق تعديل او سحب الخدمة في أي وقت.

تتم الموافقة على التعليقات المرسلة من المستخدمين وفق المعايير التالية:
العلاقة المباشرة:
يجب أن يكون الشخص صاحب التعليق قد اختبر الخدمة مباشرة من خلال المحل.
المحتوى المقبول للتعليق:
تطبيق كاراميلا غير ملزمة بقبول أي من التعليقات التي يتم ارسالها عن طريق المستخدمين، ومن بعض الأسباب التي يمكن ان نرفض نشر التعليقات بسببها هي كالآتي:
التعليقات التي تحتوي على لهجة هجومية، إباحية ، اسائية ، عنصرية ، لغة الكراهية ، كلها أمـور لا يمكننا نشرها على تطبيقنا.
تعليقات ليس لها علاقة بموضوع تقييم المحل.
التعليقات التي تتضمن التمـييز القائم على الـدين، العـرق، الجنس، العمر، الحالة الزوجية أو الإعـاقة الجسدية او الفكرية.
أي محتوى له دلالات أو اشارات تخالف تقاليد المجتمع، أو تدعو الى توجه غير قانوني.
تضارب المصالح : حيث لا يُسـمح للمحلات المنافسة وضع أي تعليقات سلبية. (أي مخالفات بهذا الصدد، ممكن أن تؤدي إلى إزالة دائمة لتلك المحلات من تطبيقنا).
الادعاءات المتعلقة بمخالفة قوانين الصحة.
التعليقات التي تعكس معلومات قديمة وغير نافذة.
أصحاب التعليقات لا يمثلون رأي تطبيق كاراميلا على الإطلاق ولا الشركة المالكة الرئيسية، ولا أي من الشركات التابعة. لتطبيق كاراميلا غير مسـؤول عن أي إلتزامات أو مطالبات أو خسائر ناتجة عن أي من التعليقات المنشورة أو أي من المحتوى الموجود على التطبيق.

ملكية تطبيق كاراميلا:
التعليقات المنشورة على التطبيق هي ملك لتطبيق كاراميلا وملكيتها حصرية إلى الأبد. لدينا الحق في إعادة إنتاج وتعديل وترجمة ونقل و/أو توزيع جميع المواد المتعلقة بالتعليقات. تطبيق كاراميلا ليست ملزمة بأي شكل من الأشكال بالدفع لأصحاب التعليقات أو لأي شخص أو جهة اخــرى مقابل تعليقاتهم.
يجوز ان تقوم تطبيق كاراميلا بإنهاء هذه الاتفاقية من جانب واحد في أي وقت.

توافق على ان عواقب الاستخدام التجاري او اعادة نشر محتوى او المعلومات من التطبيق بدون تصريح من الشركة قد تكون جسيمة ولا يمكن تقديرها بحيث لا يكون التعويض النقدي كافي او ملائم وإن تطبيق كاراميلا سوف يحق لها اتخاذ قرار مؤقت ودائم لحظر هذا الاستخدام.
اثناء أي عملية توصيل للمنتج يتم القيام بها بواسطة المحلات، وفي حالة عدم تواجد العميل في العنوان المحدد في النظام، لن يتم تسليم البضاعة التي طلبها العميل الى أي مكان اخر. وفي هذه الحالة، يتعين على العضو قبول كافة المسؤوليات القانونية الناشئة عن طلب غرض إلى عنوان لا يقيم فيه.

بالنسبة إلى اعادة المنتج، يتم تطبيق شروط اعادة المنتج الخاصة بالمحلات. إن تطبيق كاراميلا لا تتحمل أي مسؤوليات عن النزاعات التي قد تنشأ عن شروط اعادة المنتج الخاصة بالمحلات.

1.	يستطيع العميل النهائي الاختيار بين طرق الدفع المختلفة على لتطبيق كاراميلا، وهى في الوقت الحالي: نقداً 
2. يحق لتطبيق كاراميلا توفير اي طرق دفع اخرى غير متاحة حالياً، أو حجب اي طرق دفع متوفرة حاليا
3. في حال الدفع عن طريق الانترنت، يلتزم العميل النهائي باختيار احد طرق الدفع المتاحة عند الطلب، حيث سيتم التعامل مع عمليات الدفع عبر الانترنت من خلال مزودي خدمة الدفع عبر الانترنت
4. . فقط في حال وافق العميل مسبقاً على ذلك، سيقوم مزودوا خدمة الدفع عبر الانترنت بالاحتفاظ ببيانات بطاقة العميل لاستخدامها فى حال الطلب لاحقا دون الحاجة الى ادخال البيانات البطاقة الائتمان عند كل عملية دفع
5. . يلتزم العميل دوماً بضمان ان رصيد البطاقة كافٍ لدفع قيمة الطلب وذلك قبل القيام بالطلب، كما يلتزم العميل باستخدام البطاقة ضمن الحد الائتمانى المسموح للبطاقة. كما يلتزم العميل بعدم التورط بعمليات رد المبالغ المدفوعة الغير مصرح بها.

إن تطبيق كاراميلا تتعامل مع رضا العملاء بمنتهى الجدية. وفي حالة وجود مشاكل مع طلب أي غرض، يرجى الاتصال بـتطبيق كاراميلا عبرالهاتف لدينا أو بالاتصال بنا عبر الهاتف وسوف نقوم بمساعدتك ونحاول حل المشكلة، وفي الحالات التي تم فيها اصدار فاتورة لك عن طريق تطبيق كاراميلا ، سوف تقوم تطبيق كاراميلا برد كامل او جزء من المبلغ طبقا للحالات التالية: في حالة عدم حصولك على الطلب او استلام طلب غير صحيح، يجوز اصدار رد المبلغ كاملا لك، وفي حالة نقص جزء من طلبك، سوف نقوم باصدار رد جزء من المبلغ، وفي جميع الحالات، سوف نبذل قصارى جهدنا لضمان رضائك عن خدمة تطبيق كاراميلا.

عند قبول طلبك، فإن ذلك يمثل اتفاق بينك كعميل وبين المحل، وتقع على المحل وحده المسؤولية عن هذا الطلب. ويتم الدفع النقدي مباشرةً الى المحل الذي يقوم بتلبية الطلب. وفي حالة اختيارك للدفع عن طريق بطاقة ائتمان أو الدفع المسبق عبر التطبيق، عندئذ سيتم تنفيذ المعاملة من خلال تطبيق كاراميلا كوسيط بينك وبين المحل، وسوف يتم احتساب رسوم التوصيل أو أي رسوم اخرى لكل طلب وبناءا على سياسة كل محل وأي رسوم توصيل أو رسوم أخرى سوف يتم عرضها بوضوح في ملخص الطلب.
بطاقات الدفع المسبق / الائتمان:
يرجى قراءة الشروط والأحكام الخاصة بالخدمة قبل استخدام خدمة بطاقة الدفع المسبق / الائتمان:
إن العميل مسؤول عن الدفع إلى المحل، وقد يقوم المحل بفرض رسوم إضافية في حال أضاف العميل "طلب خاص" أو "طلب عام" إلى طلبه، وسوف يتم الدفع نقداً لهذه الطلبات الإضافية عند توصيل الطلب.
يحق للعميل إلغاء الطلب في حالة تجاوز زمن التوصيل أكثر من زمن التوصيل المحدد من قبل المحل ، وسوف يتم رد المبلغ الذي قام العميل بدفعه الى حساب العميل.
يحق للعميل إلغاء الطلب خلال 5 دقائق بحد اقصى من قبول الطلب.
قد تستغرق عملية استرداد المبلغ لحساب العميل عبر بوابة الدفع الخاصة ببطاقة الدفع المسبق / الائتمان مدة 2-7 أيام عمل، وفي حال أي تأخير في إعادة قيد المبلغ لحساب العميل الذي قام بدفعه، يتوجب على العميل المتابعة مع البنك مباشرة وسوف نرسل رسالة إلكترونية للعميل تحتوي على نسخة مطبوعة من إشعار رد المبلغ المطبوع وذلك من بوابة الدفع الخاصة ببطاقة الدفع المسبق / الائتمان كمرجع له في حالة ما إذا رغب العميل في مراجعة البنك .
إن العملاء المستخدمين لخدمة الدفع الخاصة ببطاقة الدفع المسبق / الائتمان مطالبين بالتواجد على أرقام الاتصال الخاصة بهم.
سوف يتم تعويض العميل من قبل المحل في حالة نقص أي جزء من الطلب (يسري التسديد فقط مقابل البند الناقص ويخضع التعويض للسياسة الخاصة بالمحل)
بطاقات الإئتمان أو الدفع المسبق التي يستخدمها العميل في السداد من خلال بوابة الدفع الإلكتروني على تطبيق أو تطبيقات تطبيق كاراميلا يتوجب أن تعود ملكيتها للعميل نفسه. في حال كانت ملكية البطاقة تعود لشخص آخر فيتوجب على العميل إستخدامها بصورة قانونية وذلك بموافقة صاحب البطاقة.
ان العميل مسؤول بالكامل عن تنفيذ وتقديم الطلب باستخدام خدمة الدفع الخاصة ببطاقة الدفع المسبق / الائتمان بعد القراءة الدقيقة لكافة الشروط والاحكام.

في حالة النزاعات التي قد تنشا بالارتباط مع هذه الاتفاقية، يوافق العضو ويقر ان ادخال أي قيود وميكروفيلم وميكروفيش وسجلات كمبيوتر تشكل دليل ساري المفعول ونافذ ومطلق وشامل، ان هذه المادة لها خصائص عقد الاثبات وان العضو يتنازل مسبقا عن كافة انواع الاعتراضات على سجلات تطبيق كاراميلا بالارتباط مع حقيقة ان هذه السجلات يتم الاحتفاظ بها بالشكل الواجب وفقاً للقانون.

تخضع شروط هذا التطبيق وتفسر وفقا للقانون. وسوف تخضع النزاعات الناشئة والمتعلقة بشروط التطبيق الماثلة (بما في ذلك النزاعات غير التعاقدية) الى الاختصاص الحصري للمحاكم.

ي حالة اعتبار أي جزء من شروط الاستخدام الماثلة غير قانونية او باطلة او غير قابلة للتنفيذ لاي سبب، عندئذ سوف يعتبر هذا الحكم قابل للفصل عن شروط الاستخدام الماثلة ولن تؤثر على صلاحية وقابلية تنفيذ أي من الاحكام المتبقية لشروط الاستخدام الماثلة. ولن يتم تفسير أي تنازل من جانبنا كتنازل عن أي انتهاك سابق او لاحق لاي حكم.

تمنح تطبيق كاراميلا لك التصريح (الذي يجوز الغاؤه في أي وقت لاي سبب وبدون سبب) لمشاهدة التطبيق وللتحميل والتراسل الالكتروني والمشاركة عبر الشبكات الاجتماعية او طباعة الصفحات الفردية من التطبيق وفقا لشروط الاستخدام الماثلة وفقط لاستخدامك الشخصي غير التجاري، شريطة عدم قيامك بإزالة أي علامة تجارية، اشعار حقوق نشر او اشعار اخر مشمول في هذه الصفحات. ولا يسمح بأي استخدام اخر. ولا يجوز ان تقوم، على سبيل المثال، بتضمين المعلومات والمحتوى او المواد الاخرى في أي قاعدة بيانات، مؤلف، ارشيف او ذاكرة مؤقتة. ولا يجوز ان تقوم بتعديل، نسخ، توزيع، اعادة نشر، ارسال، عرض، اداء، اعادة انتاج، نشر، اعادة استخدام، اعادة بيع، ترخيص، انشاء اعمال مشتقة من، تحويل او بيع أي معلومات، محتوى، مواد، برمجيات، منتجات او خدمات يتم الحصول عليها من التطبيق، باستثناء ما هو مذكور تحديدا اعلاه. وباستثناء ما هو مصرح تحديدا من تطبيق كاراميلا ، لا يجوز ان تقوم بالربط بالتطبيق لاي غرض او الولوج الى التطبيق يدويا او عن طريق أي robot, spider, web crawler, extraction software او أي عملية اتوماتيكية او أداة لغرض كشط، نسخ او مراقبة أي جزء من التطبيق او أي معلومات، محتوى، او مواد على المواقع. وتحتفظ تطبيق كاراميلا بكافة حقوقها بموجب التشريع والقانون العام ضد أي شخص او جهة تنتهك هذه الفقرة. ولا يجوز ان تقوم بالربط او الوصل بأي صفحات بالتطبيق أو أي محتوى مشمول فيها، سواء بالكامل او جزئيا، بدون الموافقة الخطية المسبقة من تطبيق كاراميلا. وقد تشارك الوصلات مع التطبيق عن طريق عن طريق المواقع الإجتماعية الموجودة على التطبيق . وإن أي حقوق اخرى لم يتم منحها صراحة في هذه الشروط محتفظ بها.

ان المحتوى على تطبيق الشبكة (محتوى تطبيق كاراميلا) والعلامات التجارية وعلامات الخدمة والشعارات المشمولة في هذه الشروط (العلامات) مملوكة لدى تطبيق كاراميلا او تم ترخيصها الى تطبيق كاراميلا وتخضع الى حقوق النشر وحقوق الملكية الفكرية الاخرى في دولة الكويت والمعاهدات الدولية. ان محتوى تطبيق كاراميلا يتضمن بدون حصر كافة محتوى البرمجيات الأساسي وقواعد البيانات والوظائف والبرمجيات وتصميمات التطبيق على الشبكة والصوت والفيديو والنص والصور الفوتوغرافية والرسوم البيانية. ان كافة الرسوم البيانية والشعارات والتصميمات ورؤوس الصفحات والأيقونات والنصوص واسماء الخدمة العائدة الى تطبيق كاراميلا هي علامات تجارية مسجلة وعلامات تجارية بموجب القانون العام او شكل تجاري لـتطبيق كاراميلا في الكويت و/أو بلدان اخرى. ولا يجوز استخدام العلامات التجارية والشكل التجاري العائد الى تطبيق كاراميلا بما في ذلك كجزء من العلامات التجارية و/أو كجزء من اسماء النطاق، بالارتباط مع أي منتج او خدمة بأي اسلوب من المحتمل ان يسبب ارتباك ولا يجوز نسخها، تقليدها، او استخدامها سواء بالكامل او جزئيا بدون تصريح خطي مسبق من تطبيق كاراميلا. ان محتوى تطبيق كاراميلا على تطبيق الشبكة يتم تقديمه إليك "كما هو" لمعلوماتك واستخدامك الشخصي فقط ولا يجوز استخدامه، نسخه، اعادة انتاجه، توزيعه، نقله، بثه، عرضه، بيعه، ترخيصه او بخلاف ذلك استغلاله لاي أغراض اخرى ايا كانت، بدون التصريح الخطي المسبق من المالكين المعنيين. ان الاستعادة النظامية للبيانات أو أي محتوى آخر من التطبيق او لانشاء او تأليف، سواء بشكل مباشر او غير مباشر، مجموعة، مؤلف، قاعدة بيانات او دليل بدون التصريح الخطي من تطبيق كاراميلا محظور.
وبما انك مؤهل لاستخدام التطبيق ، يتم منحك ترخيص محدود للولوج الى واستخدام التطبيق على الشبكة ومحتوى تطبيق كاراميلا وتنزيل او طباعة نسخة عن أي جزء من محتوى تطبيق كاراميلا الذي حصلت على حرية الدخول اليه بشكل صحيح فقط للاستخدام الشخصي غير التجاري. وتحتفظ تطبيق كاراميلا بكافة الحقوق التي لم يتم منحها صراحة اليك في وعلى تطبيق الشبكة ومحتوى وعلامات تطبيق كاراميلا. وفي حالة قيامك بتنزيل او طباعة نسخة عن محتوى تطبيق كاراميلا للاستخدام الشخصي، يتعين عليك الاحتفاظ بكافة اشعارات حقوق النشر واشعارات الملكية الاخرى المشمولة فيها. وتوافق على عدم تجاوز وتبطيل او بخلاف ذلك التدخل في الخصائص الامنية الخاصة بتطبيق الشبكة او الخصائص التي تمنع او تقيد استخدام او نسخ أي من محتوى تطبيق كاراميلا او تنفيذ القيود على استخدام التطبيق على الشبكة او محتوى تطبيق كاراميلا.

عن طريق نشر أي مساهمات في أي جزء من التطبيق على الشبكة، فإنك تلقائيا تتعهد وتضمن ان لديك الحق لمنح تطبيق كاراميلا ترخيص غير قابل للنقض ودائم وغير حصري وقابل للتحويل وخالي من الاتعاب ومدفوع بالكامل في جميع انحاء العالم لاستخدام ونسخ وتقديم الأداء والعرض العام واعادة صياغة وترجمة واقتباس (بالكامل او جزئيا) وتوزيع هذه المساهمات لاي غرض، كالإعلان التجاري أو خلاف ذلك او بالارتباط مع التطبيق على الشبكة او ترويجها، لاعداد الاعمال المشتقة عن هذه المساهمات او تضمينها في اعمال اخرى، ومنح واعتماد اصدار التراخيص الفرعية للمذكور اعلاه.

يحق لـتطبيق كاراميلا – وفقا لتقديرنا وسلطتنا المطلقة– ان (1) تقوم بتحرير وصياغة أي من المساهمات او بخلاف ذلك تغييرها (2) اعادة تصنيف أي مساهمات لوضعها في مواقع ملائمة بقدر اكبر او (3) الفرز المسبق او حذف أي مساهمات التي يتقرر انها غير ملائمة او تنتهك هذه الاتفاقية.

كخدمة إضافية، قد يقدم التطبيق روابط مع مواقع اخرى على الشبكة. وبعض من هذه المواقع على الشبكة قد تكون تابعة لتطبيق كاراميلا بينما لا يكون البعض الاخر كذلك. ان تطبيق كاراميلا غير مسؤولة عن محتويات أي صفحات تطبيق على الشبكة تم انشاؤها والحفاظ عليها بمعرفة مؤسسات مستقلة عن تطبيق كاراميلا. ان زيارة أي صفحات مواقع اطراف ثالثة يكون على مسؤوليتك الخاصة. وليس لـتطبيق كاراميلا أي سيطرة على صفحات مواقع الشبكة الاخرى المذكورة، ولا يمكنها ان تضمن دقة وكمالية او توقيت المعلومات الواردة في صفحات مواقع الغير. ان استخدامك لهذه المعلومات طوعي واعتمادك على هذه المعلومات يجب ان يتم فقط بعد المراجعة المستقلة. ان الاشارات الى المنتجات التجارية او الخدمات ضمن أي صفحات مواقع الغير المذكورة لا يشكل او يعني موافقة تطبيق كاراميلا. وعن طريق استخدام التطبيق، تقر ان تطبيق كاراميلا ليست مسؤولة عن توفر المواقع الأخرى او المحتوى الكائن في او عبر أي صفحات المواقع الأخرى.

للحد المسموح به بموجب القانون، تقدم تطبيق كاراميلا هذا المحتوى والتطبيق على اساس "كما هو" و"كما هو متاح" ولا نقدم أي تعهد او ضمان من أي نوع، صريح كان ام ضمني، بخصوص محتوى او توفر هذا التطبيق، او انه سوف يكون في الوقت المحدد او خالي من الاخطاء، وأن العيوب سيتم تصحيحها، او ان التطبيق او الخادم الذي يقوم بتوفيره خالي من الفيروسات او المكونات الضارة الاخرى.
وبالخضوع الى الفقرة السابقة، فإن تطبيق كاراميلا أو مدراء ومسؤولي ووكلاء وموظفي او مقاولي تطبيق كاراميلا ليس عليهم أي مسؤولية تجاه أي خسائر او اضرار مباشرة أو غير مباشرة، خاصة او استتباعية (بما في ذلك وبدون حصر الاضرار عن خسارة العمل او خسارة الارباح)، التي تنشأ عن العقد، أو أي تقصير او خلاف ذلك من استخدام او عدم القدرة على استخدام هذا التطبيق. ولن يستثنى أي شيء من هذه الشروط او يقيد المسؤولية عن الوفاة او الاصابة الشخصية الناتجة عن اهمالنا او مسؤولية وكلائنا او موظفينا.

ت توافق على تعويض وتجنيب تطبيق كاراميلا وشركتها الام وشركاتها التابعة والزميلة ووكلائها ومسؤوليها ومدرائها او الموظفين من أي مطالبة، طلب، ضرر (سواء كان مباشر او غير مباشر او استتباعي) بما في ذلك اتعاب المحامي التي يتم تقديمها بواسطة أي شخص مرتبط باستخدامك للتطبيق، مع تقديماتك، مع أي انتهاك مزعوم به لحقوق الملكية الفكرية او أي حقوق اخرى لاي شخص او جهة اخرى ترتبط بالتطبيق، ومخالفتك لشروط الاستخدام الماثلة واي تصرفات اخرى او اغفالات تتعلق بالتطبيق.

ان المعلومات والمحتوى والمنتجات والخدمات والمواد المتوفرة من خلال المواقع (سواء تم تقديمها بمعرفة تطبيق كاراميلا ، من خلالك، من خلال المستخدمين الاخرين او اطراف زميلة اخرى/اطراف ثالثة) بما في ذلك وبدون حصر الطلبات، التقديمات، النص، الصور الفوتوغرافية، الرسوم البيانية، الملفات الصوتية، الفيديو والروابط، مقدمة "كما هي" وكما هي متاحة بدون ضمانات من أي نوع، صريحة كانت ام ضمنية لاقصى حد مسموح به بموجب القانون. ان تطبيق كاراميلا تتخلى عن كافة التعهدات والضمانات، صريحة كانت ام ضمنية، بما في ذلك ولا يقتصر على ضمانات قابلية التسويق، الملائمة لغرض معين، الملكية، عدم الانتهاك، الخلو من فيروسات الكمبيوتر، والضمانات الضمنية الناشئة عن سياق التعامل او سياق الاداء.
                  
                  
                  
                  
